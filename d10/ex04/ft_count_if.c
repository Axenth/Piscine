int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int i2;

	i = 0;
	i2 = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
			i2++;
		i++;
	}
	return (i2);
}
